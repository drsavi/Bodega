package bodeguinha;

public class Funcionario{
    public int codigo;
    public String nome;
    public int idade;
    public double salario;

    Funcionario(){
        
    }

    Funcionario(int codigo, String nome, int idade, double salario){
        this.codigo = codigo;
        this.nome = nome;
        this.idade = idade;
        this.salario = salario;
    }
}




